#include<stdio.h>
int main()
{
    //for character type
    char pattern;
    printf("Enter pattern: ");
    scanf("%c",&pattern);
    
    //for integer type
    int age;
    printf("Enter age: ");
    scanf("%d",&age);

    //for float type
    float pi;
    printf("enter value of pi: ");
    scanf("%f",&pi);

    return 0;
}